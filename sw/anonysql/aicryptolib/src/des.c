#define SBOX_OP(a,key,ti,ret) \
	(ti)  = (((a)<<5)|((a)>>27))^(int)(key>>42);\
	(ret) = Sboxc[0][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>23)^(int)(key>>36);\
	(ret)|= Sboxc[1][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>19)^(int)(key>>30);\
	(ret)|= Sboxc[2][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>15)^(int)(key>>24);\
	(ret)|= Sboxc[3][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>11)^(int)(key>>18);\
	(ret)|= Sboxc[4][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>7)^(int)(key>>12);\
	(ret)|= Sboxc[5][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = ((a)>>3)^(int)(key>>6);\
	(ret)|= Sboxc[6][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];\
	(ti)  = (((a)<<1)|((a)>>31))^(int)(key);\
	(ret)|= Sboxc[7][(((ti)>>4)&0x02)|((ti)&0x01)][((ti)>>1)&0x0f];


#define BIT_EXCHG(a,b,reg,sh,msk)\
	(reg)=((((a)>>(sh))^(b))&(msk));\
	(b)^=(reg); (a)^=((reg)<<(sh));

void IP1(ULONG* a,ULONG* b){
	ULONG reg,l=*a,r=*b;
	BIT_EXCHG(l,r,reg,4, 0x0f0f0f0fL);
	BIT_EXCHG(l,r,reg,16,0x0000ffffL);
	BIT_EXCHG(r,l,reg,2, 0x33333333L);
	BIT_EXCHG(r,l,reg,8, 0x00ff00ffL);
	BIT_EXCHG(l,r,reg,1, 0x55555555L);
	*a=l;*b=r;
}

void IP2(ULONG *a, ULONG* b){
	ULONG reg,l=*a,r=*b;
	BIT_EXCHG(l,r,reg,1, 0x55555555L);
	BIT_EXCHG(r,l,reg,8, 0x00ff00ffL);
	BIT_EXCHG(r,l,reg,2, 0x33333333L);
	BIT_EXCHG(l,r,reg,16,0x0000ffffL);
	BIT_EXCHG(l,r,reg,4, 0x0f0f0f0fL);
	*a=l;*b=r;
}

void des_crypto(ULONG* a, ULONG* b, ULLONG *keyL, int enc){
	ULLONG key;
	ULONG l=*a,r=*b,tmp;
	int i,ti,j;

    if(enc){
		for(i=0;i<16;i++){
			key = keyL[i];
			SBOX_OP(r,key,ti,j);

			tmp = l ^ j;
			l = r;
			r = tmp;
	    }
	}else{
	    for(i=15;i>=0;i--){
			key = keyL[i];
			SBOX_OP(l,key,ti,j);
	
			tmp = r ^ j;
			r = l;
			l = tmp;
		}
	}
	*a=l;*b=r;
}

/*------------------------------------------------
    1 block(64bit)
------------------------------------------------*/
ULLONG DES2Crypto(ULLONG blockL,ULLONG *keyL){
	ULONG l,r;
    ULLONG	retL;

    l = (ULONG)(blockL >> 32);
    r = (ULONG) blockL;
	IP1(&l,&r);

#ifdef __WINDOWS__
	des_crypto_asm(&l,&r,keyL,0,16,1);
#else
	des_crypto(&l,&r,keyL,1);
#endif

	IP2(&r,&l);
	retL = (ULLONG)r << 32;
	retL |= l;

    return retL;
}

/*------------------------------------------------
    1 block(64bit)
------------------------------------------------*/
ULLONG DES2Plain(ULLONG blockL,ULLONG *keyL){
    ULONG l,r;
    ULLONG	retL;

    r = (ULONG)(blockL >> 32);
    l = (ULONG) blockL;
	IP1(&r,&l);

#ifdef __WINDOWS__
	des_crypto_asm(&r,&l,keyL,15,-1,-1);
#else
	des_crypto(&l,&r,keyL,0);
#endif

   	IP2(&l,&r);
    retL = (ULLONG)l << 32;
    retL |= r;

    return retL;
}
